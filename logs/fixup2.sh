# Directory Filename Title
function createFrontMatterFile() {
	local BASE=$( basename $2 )
	local FRIENDLY=$( echo $BASE | sed "s/$1-/$3 /" | sed "s/\.md//" )
	echo "<!-- TITLE: $FRIENDLY -->"
	echo "<!-- SUBTITLE: A game log for $3 -->"
	echo ""
	echo "# $FRIENDLY"
	echo ""
	cat $2
}

function addFrontMatterFile() {
	mkdir -p out/$1
	createFrontMatterFile "$@" > out/$2
}

